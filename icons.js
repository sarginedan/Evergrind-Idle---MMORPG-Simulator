/**
 * Returns an <img> tag string for use in innerHTML.
 * @param {string} key — key from ICONS
 * @param {number} size — pixel size (default 16)
 * @param {string} extraStyle — additional inline CSS
 */
export function iconImg(key, size = 16, extraStyle = '') {
    const url = ICONS[key];
    if (!url) return '';
    return `<img src="${url}" width="${size}" height="${size}" style="vertical-align:middle;object-fit:contain;${extraStyle}" alt="${key}" draggable="false">`;
}

